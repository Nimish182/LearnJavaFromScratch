public class For_Hello {

    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {       //(initailize; condition; increment)
            // This loop will run 3 times
            // The value of i will be 0, 1, 2
            // The loop will stop when i is no longer less than 3       
            // The value of i will be incremented by 1 each time the loop runs
            // The loop will print "Hello, World!" 3 times
            System.out.println("Hello, World!");
        }
    }
}